/**
 * op_add - Function that adds two integers
 * @a: First Integer
 * @b: Second Integer
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function the subtracts two integers
 * @a: First Integer
 * @b: Second Integer
 * Return: differnece of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function the multiplies two integers
 * @a: First Integer
 * @b: Second Integer
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function the divides two integers
 * @a: First Integer
 * @b: Second Integer
 * Return: Division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function that gets the remainder of two integers
 * @a: First Integer
 * @b: Seconde Integer
 * Return: The Remainder of division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
